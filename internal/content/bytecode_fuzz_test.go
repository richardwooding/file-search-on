// FuzzParseClassFile targets the JVM .class constant-pool walker.
// Variable-length entries with type-driven sizes are classic
// fuzz-vulnerable territory: a malformed CP entry could trigger
// out-of-bounds reads, infinite loops, or excessive allocation if
// the walker doesn't bounds-check every read. Contract: never panic;
// returned attrs are valid Go values (no nil deref).
func FuzzParseClassFile(f *testing.F) {
	seeds := [][]byte{
		{},
		{0xCA, 0xFE, 0xBA, 0xBE},
		// Header + zero pool count — well-formed empty class.
		{0xCA, 0xFE, 0xBA, 0xBE, 0x00, 0x00, 0x00, 0x3D, 0x00, 0x01},
		// Header with bogus pool count.
		{0xCA, 0xFE, 0xBA, 0xBE, 0x00, 0x00, 0x00, 0x3D, 0xFF, 0xFF},
		// Header + claimed CP entry beyond buffer.
		{0xCA, 0xFE, 0xBA, 0xBE, 0x00, 0x00, 0x00, 0x3D, 0x00, 0x02, 0x01, 0xFF, 0xFF},
		// Class with Long entry that takes two pool slots (must
		// handle the i++ skip correctly).
		{0xCA, 0xFE, 0xBA, 0xBE, 0x00, 0x00, 0x00, 0x3D, 0x00, 0x03, 0x05,
			0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
	}
	for _, s := range seeds {
		f.Add(s)
	}
	f.Fuzz(func(t *testing.T, data []byte) {
		_ = parseClassFile(data)
	})
}

// FuzzParsePYCHeader targets the Python .pyc header parser. Magic
// number table lookup + LE byte parsing + timestamp arithmetic
// shouldn't panic on any input.
func FuzzParsePYCHeader(f *testing.F) {
	seeds := [][]byte{
		{},
		{0xA7, 0x0D, 0x0D, 0x0A},                                        // magic only
		{0xA7, 0x0D, 0x0D, 0x0A, 0x00, 0x00, 0x00, 0x00},                // PEP 552 timestamp flag, truncated
		{0xA7, 0x0D, 0x0D, 0x0A, 0x01, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF}, // hash-based
		{0xFF, 0xFF, 0xFF, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00}, // unknown magic
	}
	for _, s := range seeds {
		f.Add(s)
	}
	f.Fuzz(func(t *testing.T, data []byte) {
		_ = parsePYCHeader(data)
	})
}

// FuzzParseWASMModule targets the WebAssembly section walker. LEB128
// size prefixes + section-id dispatch + bounded reads are all
// historically fuzz-vulnerable. Section spam, oversized claimed
// sizes, and overlong LEB128 encodings are explicitly defended in
// the parser; the fuzz target confirms.
func FuzzParseWASMModule(f *testing.F) {
	seeds := [][]byte{
		{},
		{0x00, 0x61, 0x73, 0x6D, 0x01, 0x00, 0x00, 0x00}, // header only
		// Header + one type section.
		{0x00, 0x61, 0x73, 0x6D, 0x01, 0x00, 0x00, 0x00,
			0x01, 0x04, 0x01, 0x60, 0x00, 0x00},
		// Header + import section with overlong LEB128 (10 bytes of 0x80).
		{0x00, 0x61, 0x73, 0x6D, 0x01, 0x00, 0x00, 0x00,
			0x02, 0x0A, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x80, 0x00},
		// Header + section with claimed size larger than file.
		{0x00, 0x61, 0x73, 0x6D, 0x01, 0x00, 0x00, 0x00,
			0x07, 0xFF, 0x7F},
	}
	for _, s := range seeds {
		f.Add(s)
	}
	f.Fuzz(func(t *testing.T, data []byte) {
		_ = parseWASMModule(data)
	})
}
